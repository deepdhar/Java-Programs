class StrArray {
	public static void main(String args[]) {
		String[] strArray = {
				"A",
				"Quick",
				"Brown",
				"Fox",
				"Jumps",
				"Over",
				"The",
				"Lazy",
				"Dog"
		};
		
		System.out.println("The values stored in array are:");
		
		for(int i=0; i<strArray.length; i++) {
			System.out.println(strArray[i] + " ");
		}
	}
}
